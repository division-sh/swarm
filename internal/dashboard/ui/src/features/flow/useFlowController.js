import { useMemo } from "react";

export function useFlowController({
  verticals,
  visibleFlowEvents,
  flowEvents,
  flowGraph,
  flowGraphMeta,
  flowActiveEdgeKeys,
  selectedFlowSummary,
  agents,
  flowView,
  flowStage,
  flowStageOptions,
  flowRubric,
  flowRubricOptions,
  flowVertical,
  flowStart,
  flowEnd,
  flowReplaySpeed,
  flowReplayOn,
  flowReplayIndex,
  selectedFlowNodeID,
  selectedFlowEdgeID,
  flowViewGraph,
  graphFullscreen,
  setFlowView,
  setFlowStage,
  setFlowRubric,
  setFlowVertical,
  setFlowStart,
  setFlowEnd,
  setFlowReplaySpeed,
  setFlowReplayOn,
  setFlowReplayIndex,
  refresh,
  addToast,
  setSelectedFlowNodeID,
  setSelectedFlowEdgeID,
  setFlowViewGraph,
  setGraphFullscreen,
}) {
  return useMemo(() => ({
    state: {
      verticals,
      visibleFlowEvents,
      flowEvents,
      flowGraph,
      flowGraphMeta,
      flowActiveEdgeKeys,
      selectedFlowSummary,
      agents,
      flowView,
      flowStage,
      flowStageOptions,
      flowRubric,
      flowRubricOptions,
      flowVertical,
      flowStart,
      flowEnd,
      flowReplaySpeed,
      flowReplayOn,
      flowReplayIndex,
      selectedFlowNodeID,
      selectedFlowEdgeID,
      flowViewGraph,
      graphFullscreen,
    },
    actions: {
      setFlowView,
      setFlowStage,
      setFlowRubric,
      setFlowVertical,
      setFlowStart,
      setFlowEnd,
      setFlowReplaySpeed,
      setFlowReplayOn,
      setFlowReplayIndex,
      refresh,
      addToast,
      setSelectedFlowNodeID,
      setSelectedFlowEdgeID,
      setFlowViewGraph,
      setGraphFullscreen,
    },
  }), [
    addToast,
    agents,
    flowActiveEdgeKeys,
    flowEnd,
    flowEvents,
    flowGraph,
    flowGraphMeta,
    flowReplayIndex,
    flowReplayOn,
    flowReplaySpeed,
    flowRubric,
    flowRubricOptions,
    flowStage,
    flowStageOptions,
    flowStart,
    flowVertical,
    flowView,
    flowViewGraph,
    graphFullscreen,
    refresh,
    selectedFlowEdgeID,
    selectedFlowNodeID,
    selectedFlowSummary,
    setFlowEnd,
    setFlowReplayIndex,
    setFlowReplayOn,
    setFlowReplaySpeed,
    setFlowRubric,
    setFlowStage,
    setFlowStart,
    setFlowVertical,
    setFlowView,
    setFlowViewGraph,
    setGraphFullscreen,
    setSelectedFlowEdgeID,
    setSelectedFlowNodeID,
    verticals,
    visibleFlowEvents,
  ]);
}
